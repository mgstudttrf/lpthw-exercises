def print_two(*args):
    arg1, arg2 = args
    print(f"arg1 {arg1} arg2 {arg2}")

def print_two2(arg1, arg2):
    print(f"func2: {arg1} {arg2}")

def printone(arg):
    print(f"{arg}")

def printnone():
    print("nofin here")

print_two("hdhdh", "f1")
print_two2("ushshs, j", "f2")
printone("ydhdhhdhddf3")
printnone()
